public class excersizetwo {
	public static void main(String[] args) {
		int num = 10; //gives the end number for the loop
		for (int i = 1; i<=num; i++) {//loop that repeats ten times
			 int j=i*i; // the number multiplied by itself
			
			System.out.print(j); // prints the output of the number multiplied by itself
			System.out.print(" "); //prints space for stylistic purpose
		}
			
	}
}
